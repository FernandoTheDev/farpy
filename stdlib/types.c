#include <stdio.h>
#include <stdbool.h>

// Float to Double Conversion
double ftod(float x)
{
    return (double)x;
}

// Int to Double Conversion
double itod(int x)
{
    return (double)x;
}

// Int to Float Conversion
float itof(int x)
{
    return (float)x;
}

// Double to Float Conversion
float dtof(double x)
{
    return (float)x;
}

// Double to Int Conversion
int dtoi(double x)
{
    return (int)x;
}

// Float to Int Conversion
int ftoi(float x)
{
    return (int)x;
}

// Bool to Int Conversion
int btoi(bool x)
{
    return (int)x;
}
